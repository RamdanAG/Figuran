// Helper function to get figure by ID
export const getFigureById = (id) => {
  return figuresData.find(figure => figure.id === id);
};

// Helper function to get all figure IDs
export const getAllFigureIds = () => {
  return figuresData.map(figure => figure.id);
};

// Helper function to get figure by category
export const getFiguresByCategory = (category) => {
  return figuresData.filter(figure => figure.category === category);
};